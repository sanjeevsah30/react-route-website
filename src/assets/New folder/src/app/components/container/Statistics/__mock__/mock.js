export const generateTopicDuration = (topics, reps) => {
    let topicData = {};
    topics.forEach((topic) => {
        topicData[topic.id] = Object.keys(reps).map((rep) => ({
            speaker_id: rep,
            duration_avg: +(Math.random() * (0 - 10) + 10).toFixed(2),
            max: +(Math.random() * (0 - 20) + 20).toFixed(2),
            presence_per_call: +(Math.random() * (0 - 1) + 1).toFixed(2),
            meeting__owner_id: rep,
        }));
    });
    return topicData;
};

